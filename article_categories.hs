removeIDNineCategory :: (Integral a, Integral b) => [(a, b)] -> [(a, b)]
removeIDNineCategory ((a,b):xs)
    | xs == [] && b == 9 = []
    | xs == [] = [(a,b)]
    | b == 9 = removeIDNineCategory xs
    | otherwise = [(a,b)] ++ removeIDNineCategory xs

--reverse list to make first appearance unique....
makesFirstEntryUnique :: (Integral a, Integral b) => [(a, b)] -> [(a, b)]
makesFirstEntryUnique ((a,b):xs)
    | xs == [] = [(a, b)]
    | b == 9 = makesFirstEntryUnique xs
    | a `elem` firster xs = makesFirstEntryUnique xs
    | otherwise = [(a, b)] ++ makesFirstEntryUnique xs

firster :: (Eq a, Eq b) => [(a, b)] -> [a]
firster (x:xs)
    | xs == [] = [fst x] 
    | otherwise = [fst x] ++ firster xs

